#include<bits/stdc++.h>
using namespace std;
int main()
{
    cout << "Hello"<<"\n";
    int x;
    cout << "Enter the digits : ";
    cin >> x;
    cout << "\n";
    switch(x){
        case 1 : cout << "One";
        break;
        case 2 : printf("Two");
        break;
        case 3 : printf("Three");
        break;
        case 4 : printf("Four");
        break;
        case 5 : printf("Five");
        break;
        default: printf("None");

    }
    return 0;
}